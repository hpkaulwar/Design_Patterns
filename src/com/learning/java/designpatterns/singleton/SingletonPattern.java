package com.learning.java.designpatterns.singleton;

public class SingletonPattern {
/*
 * Creational Design Pattern
 * Singleton Pattern Restricts the instantiation of a class and ensures that only one instance of 
 * the class exists in Java Virtual Machine
 * Singleton pattern is used for logging, driver objects, caching and thread pool.
 * Singleton pattern must provide global access point to get the instance of the class
 * Singleton design pattern is used in core Java classes e.g. java.lang.Runtime, java.awt.Desktop
 * Singleton pattern is also used in other design patterns like Abstract Factory, Builder,
 * Prototype, Facade, etc.
 */
	
/*
 * To Implement Singleton pattern:
 * Private constructor to restrict instantiation of the class from other classes
 * Private static variable of the same class that is the only instance of the class
 * Public static method that returns the instance of the class, this is the global access point for 
 * the outer world to get the instance of the singleton class
 * 
 */
}
